const mongoose = require('mongoose');

const userSchema = new mongoose.Schema({
  name: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    unique: true,
    required: true,
  },
  password: {
    type: String,
    required: true,
    minlength: [5, 'Password must exceed more than 5 characters'],
  },
});

module.exports = mongoose.model('User', userSchema);
